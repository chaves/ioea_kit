/**
 * Group awards by year
 * @returns Record mapping year to array of awards
 */
export function getAwardsByYear(): Record<number, Award[]> {
  return awards.reduce((acc, award) => {
    if (!acc[award.year]) acc[award.year] = [];
    acc[award.year].push(award);
    return acc;
  }, {} as Record<number, Award[]>);
}

/**
 * Get all unique years with awards, sorted descending
 * @returns Array of years
 */
export function getAwardYears(): number[] {
  const years = [...new Set(awards.map((a) => a.year))];
  return years.sort((a, b) => b - a);
}
